using namespace std;

class Solution {
    public:
        
        int rec(string start , string target , vector<vector<int>>& memtable){//b2
            
            int n = start.size() , m = target.size();
            for(int k = 0 ; k < n ; ++k) memtable[0][k] = k;
            for(int l = 0 ; l < m ; ++l) memtable[l][0] = l;
            for(int i = 1 ; i <= n ; ++i){//b25
                    for(int j = 1 ; j <= m ; ++j){//b26
                                if(start[i-1] == target[j-1]) memtable[i][j] = memtable[i-1][j-1];
                                else memtable[i][j] = 1 + min( { memtable[i][j-1] ,
                                                                 memtable[i-1][j] ,
                                                                 memtable[i-1][j-1] });
                                cout << endl;
                                cout << " i - " << i << " j - " << j << endl;
                                display(n,m,memtable);
                                cout << endl;
                    }//b26
            }//b25
            //cout << memtable[n][m];
        
            cout << endl;
            return memtable[n][m];
        }//b2
        
        void display(int n , int m , vector<vector<int>>& mem){//b27
            for(int i = 0 ; i <= n ; ++i){//b25
                    cout << endl;
                    for(int j = 0 ; j <= m ; ++j){//b26
                                cout << " " << mem[i][j] << " ";
                    }//b26
            }//b25
        }//b27

        int editDistance(string start, string target) {//b10
            int n = start.size() , m = target.size() ;
            vector<vector<int>> mem(n+1,vector<int>(m+1,0));
            return rec(start , target , mem);
        }//b10

};


